# puts is used to print the calculation.
puts 1 + 2
# + is the key for addition.
puts 1.0 * 2.0
# * is the key for multiplication.
puts 5.0 - 8.0
# - is the key for subtraction.
puts 9.0 / 2.0
# / is the key for division.

# Above are examples of floats (numbers with decimal points).
# Below are examples of integers (numbers without decimal points).

puts 1 + 2
puts 2 * 3
puts 5 - 8
puts 9 / 2
# By using puts the calculation answer will be printed to the console.

puts 5 * (12-8) + -15
# The () above are called Parentheses used to write more complex expressions.
# 5 multiplied by 4 = 20. 20 + -15 = 5.
puts 98 + (59872 / (13*8)) * -52
