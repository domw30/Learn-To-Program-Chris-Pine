# Groups of letters in a program are called strings.

puts 'Hello, world!'
puts ''
puts 'Good-bye.'
# Using '' or "" around text or words will create a string.

puts 'i like ' + 'apple pie.'
# This will add 'apple pie' on to 'i like '.

puts 12 + 12
# Print integer plus integer.
puts '12' + '12'
# Print string plus string.
puts '12 + 12'
# Print string.


puts 2 * 5
# Prints integer multiplied by integer.
puts '2' * 5
# Prints string (2) 5 times by using the * key (22222).
puts '2 * 5'
# Prints a string as '' has been used around the integers.

puts 'pig' * 5
# This will print 'pig' 5 times.

